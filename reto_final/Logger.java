package reto_final;
/**
 * La clase Logger proporciona una funcionalidad simple para registrar mensajes en la consola.
 */
public class Logger {
    public Logger() {
    }
    /**
     * Registra un mensaje en la consola.
     *
     * @param var0   El mensaje que se va a registrar.
     */
    static void log(String var0) {
        System.out.println(var0);
    }
}
